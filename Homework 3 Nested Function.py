
number = float(input("Pick a number 1-7: "))

if number == 7:
    print('Sunday')

elif number == 6:
    print('Saturday')

elif number == 5:
    print('Friday')

elif number == 4:
    print('Thursday')

elif number == 3:
    print('Wednesday')

elif number == 2:
    print('Tuesday')

elif number == 1:
    print('Monday')

else:
    print("Try a number from 1-7")
